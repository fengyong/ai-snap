function Test-GitHubCLI {
    if (-not (Test-Path $GH)) {
        throw "GitHub CLI 未安装。请运行: winget install --id GitHub.cli"
    }
    
    $auth = & $GH auth status 2>&1
    if ($LASTEXITCODE -ne 0) {
        throw "GitHub 未认证。请运行: & '$GH' auth login --web"
    }
}

function Get-PRNumber {
    param([int]$InputNumber)
    
    if ($InputNumber -gt 0) {
        return $InputNumber
    }
    
    # 自动获取最新的 open PR
    Write-Host "🔍 正在获取最新的 PR..." -ForegroundColor Cyan
    $prs = & $GH pr list --repo $Repo --state open --limit 1 --json number,title | ConvertFrom-Json
    
    if (-not $prs) {
        throw "没有找到开放的 PR。请先创建 PR 或指定 PR 编号。"
    }
    
    $latest = $prs[0]
    Write-Host "找到 PR #$($latest.number): $($latest.title)" -ForegroundColor Green
    
    $confirm = Read-Host "是否对该 PR 提交 Review? (Y/n)"
    if ($confirm -eq 'n' -or $confirm -eq 'N') {
        $manual = Read-Host "请输入 PR 编号"
        return [int]$manual
    }
    
    return $latest.number
}

