function Submit-Review {
    param(
        [int]$PRNumber,
        [string]$Type
    )
    
    Write-Host "📝 正在生成 Kimi Review 报告..." -ForegroundColor Cyan
    $reviewBody = Get-KimiReviewBody
    
    # 保存到临时文件
    $tempFile = [System.IO.Path]::GetTempFileName()
    $reviewBody | Out-File -FilePath $tempFile -Encoding UTF8
    
    try {
        Write-Host "📤 正在提交 Review 到 PR #$PRNumber..." -ForegroundColor Yellow
        
        $result = & $GH pr review $PRNumber `
            --repo $Repo `
            --$($Type.ToLower()) `
            --body-file $tempFile 2>&1
        
        if ($LASTEXITCODE -eq 0) {
            Write-Host "✅ Kimi Review 提交成功！" -ForegroundColor Green
            Write-Host "🔗 查看: https://github.com/$Repo/pull/$PRNumber" -ForegroundColor Blue
        } else {
            Write-Error "提交失败: $result"
        }
    }
    finally {
        Remove-Item $tempFile -ErrorAction SilentlyContinue
    }
}

